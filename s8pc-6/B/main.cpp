#define ll long long
#define ull unsigned long long

int main(void){
  ll N;
  cin>>N;
  vector<ll> A,B;
  ll ret = 0;
  for(int i = 0;i< N;i++){
    ll a,b;
    cin>>a>>b;
    A.push_back(a);
    B.push_back(b);
    ret+=(b-a);
  }
  sort(A.begin(),A.end());
  sort(B.begin(),B.end());
  ll e=A[N/2];
  ll f=B[N/2];
  for(int i = 0;i< N;i++){
    ret+=abs(A[i]-e);
    ret+=abs(B[i]-f);
  }


  cout<<ret<<endl;

  return 0;
}
