unsigned long long int factorial(unsigned long long int n){
  if(n<=1)return 1;
  return n*factorial(n-1);
}

ll solve(int H,int W,int N,const vector<string>& map,pair<int,int> s, pair<int,int> g){
  vector<vector<int>> res(H, vector<int>(W,-1));
  queue<tuple<int,int,int>> que;
  que.push({s.first, s.second, 0});
  while(!que.empty()){
    auto [y,x,cost] = que.front();
    que.pop();
    if(y<0||H<=y)continue;
    if(x<0||W<=x)continue;
    if(map[y][x]=='X')continue;
    if(res[y][x]!=-1)continue;
    res[y][x]=cost;
    vector<pair<int,int>> d={{-1,0},{1,0},{0,-1},{0,1}};
    for(int i = 0;i<d.size();i++){
      que.push({y+d[i].first, x+d[i].second, cost+1});
    }
  }
  return res[g.first][g.second];
}
    
  
int main(void){
  int H, W, N;
  cin>>H>>W>>N;
  vector<string> map(H);
  vector<pair<int,int>> cp(N+1);
  for(int i=0;i<H;i++){
    for(int j=0;j<W;j++){
      char c;
      cin>>c;
      if(c=='S'){
	cp[0]={i,j};
	map[i].push_back('.');
      }else if('0'<c&&c<='9'){
	cp[c-'0']={i,j};
	map[i].push_back('.');
      }else{
	map[i].push_back(c);
      }
    }
  }
  //  DPRINT(map);
  ll res=0;
  for(int i = 0;i< N;i++){
    res+=solve(H,W,N,map,cp[i], cp[i+1]);
  }
  cout<<res<<endl;

  

  return 0;
}
