unsigned long long int factorial(unsigned long long int n){
  if(n<=1)return 1;
  return n*factorial(n-1);
}

bool solve(vector<ll> H, vector<ll> S, ll m){
  vector<ll> tmp;
  for(int i = 0;i<H.size();i++){
    if(m-H[i] < 0)return false;
    tmp.push_back((m-H[i])/S[i]);
  }

  sort(tmp.begin(),tmp.end());

  bool ok=true;
  for(int i = 0;i<tmp.size();i++){
    if(tmp[i]<i){
      ok=false;
      break;
    }
  }
  return ok;
}
  
int main(void){
  ll N;cin>>N;
  vector<ll> H(N),S(N);
  for(int i = 0;i< N;i++){
    cin>>H[i]>>S[i];
  }
  ll l = 0,u=1000000000l*100000l;
  while(l!=u){
    ll m = (l+u)/2;
    if(solve(H,S,m)){
      u=m;
    }else{
      l=m+1;
    }
  }
  cout<<l<<endl;

  return 0;
}
