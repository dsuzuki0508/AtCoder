unsigned long long int factorial(unsigned long long int n){
  if(n<=1)return 1;
  return n*factorial(n-1);
}

int H,W;
vector<string> v;
string sunuke="snuke";
vector<vector<int>> visited;
int main(void){
  cin>>H>>W;
  v.resize(H);
  visited.resize(H);
  for(int i = 0;i<H;i++)visited[i].assign(W,-1);
  cin>>v;
  queue<pair<pair<int,int>,int>> que;
  que.push({{0,0},0});
  vector<pair<int,int>> d={{-1,0},{1,0},{0,-1},{0,1}};
  while(!que.empty()){
    pair<int,int> p = que.front().first;
    int cost = que.front().second;
    que.pop();
    if(p.first<0||H<=p.first)continue;
    if(p.second<0||W<=p.second)continue;
    if(visited[p.first][p.second] !=-1)continue;
    if(sunuke[cost%sunuke.size()] != v[p.first][p.second])continue;
    visited[p.first][p.second] = cost;
    for(const auto& e:d){
      que.push({{p.first+e.first,p.second+e.second},cost+1});
    }
  }
    
    

  if(visited[H-1][W-1]!=-1){
    cout<<"Yes"<<endl;
  }else{
    cout<<"No"<<endl;
    }

  return 0;
}
