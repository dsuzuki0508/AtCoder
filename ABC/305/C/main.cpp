template <typename T, typename U>
std::ostream& operator<<(std::ostream& os, const std::pair<T, U>& p) {
  os << "< " << p.first << ", " << p.second << " >";
  return os;
}

int main(void){
  int h,w;
  cin>>h>>w;
  vector<string> s;
  for( int i = 0;i< h;i++){
    string tmp;
    cin>>tmp;
    s.push_back(tmp);
  }
  int lh=505,uh=-3,lw=505,uw=-3;
  for(int i = 0;i< h;i++){
    for(int j = 0;j< w;j++){
      if(s[i][j] == '.')continue;
      lh = min(lh, i);
      uh = max(uh, i);
      lw = min(lw, j);
      uw = max(uw, j);
    }
  }
  for(int i = lh;i<=uh;i++){
    for(int j = lw;j<=uw;j++){
      if(s[i][j]=='.'){
	cout<<i+1<<" "<<j+1<<endl;
	return 0;
      }
    }
  }

  return 0;
}
