template<typename T, typename U>
std::pair<T,U> operator+(const std::pair<T,U>& l, const std::pair<T,U>& r){
  return {l.first+r.first, l.second+r.second};
}
template<typename T, typename U>
std::pair<T,U> operator-(const std::pair<T,U>& l, const std::pair<T,U>& r){
  return {l.first-r.first, l.second-r.second};
}
template<typename T, typename U>
std::pair<T,U> operator*(const std::pair<T,U>& l, const int n){
  return {l.first*n, l.second*n};
}
template<typename T, typename U>
std::pair<T,U> operator/(const std::pair<T,U>& l, const int n){
  return {l.first/n, l.second/n};
}
#define ll long long
#define ull unsigned long long
int main(void){
  ll N;
  vector<ll> X,Y;
  cin>>N;
  for(int i = 0;i< N;i++){
    ll x,y;
    cin>>x>>y;
    X.push_back(x);
    Y.push_back(y);
  }
  vector<ll> ret(2,0);
  for(int i = 0;i< N;i++){
    if(X[i]){
      ret[1] = max(ret[1],ret[0]+Y[i]);
    }else{
      ret[0] = max(ret[0],max(ret[0],ret[1]) + Y[i]);
    }

  }
  cout<<max(ret[0],ret[1])<<endl;

  

  return 0;
}
