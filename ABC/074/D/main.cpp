unsigned long long int factorial(unsigned long long int n){
  if(n<=1)return 1;
  return n*factorial(n-1);
}


#define INF 0x3fffffffffff
int main(void){
  ll N;
  cin>>N;
  vector<vector<ll>> edge(N, vector<ll>(N,INF));
  for(ll i =0;i<N;i++)edge[i][i]=0;
  //  vector<vector<ll>> A(N, vector<ll>(N));
  typedef tuple<ll,ll,ll> tii;
  priority_queue<tii, vector<tii>, greater<tii>> que;
  for(ll i = 0;i<N;i++){
    for(ll j = 0;j<N;j++){
      ll a;
      cin>>a;
      if(i<j)que.push({a,i,j});
    }
  }
  ll ans = 0;
  bool ok=true;
  while(!que.empty()){
    auto [a,n,m] = que.top();
    que.pop();
    if(edge[n][m]>a){
      edge[n][m]=edge[m][n]=a;
      ans+=a;
      for(ll i = 0;i<N;i++){
	for(ll j = 0;j<N;j++){
	  edge[i][j]=edge[j][i]=min({edge[i][j], edge[i][n]+a+edge[m][j], edge[j][n]+a+edge[m][i]});
	}
      }
    }else if(edge[n][m]<a){
      ok=false;
      break;
    }
  }

  if(ok){
    cout<<ans<<endl;
  }else{
    cout<<-1<<endl;
  }
  

  return 0;
}
