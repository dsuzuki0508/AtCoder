template<typename T, typename U>
std::pair<T,U> operator+(const std::pair<T,U>& l, const std::pair<T,U>& r){
  return {l.first+r.first, l.second+r.second};
}
template<typename T, typename U>
std::pair<T,U> operator-(const std::pair<T,U>& l, const std::pair<T,U>& r){
  return {l.first-r.first, l.second-r.second};
}
template<typename T, typename U>
std::pair<T,U> operator*(const std::pair<T,U>& l, const int n){
  return {l.first*n, l.second*n};
}
template<typename T, typename U>
std::pair<T,U> operator/(const std::pair<T,U>& l, const int n){
  return {l.first/n, l.second/n};
}

int main(void){
  int n;
  cin>>n;
  set<pair<int,int>> p;
  for(int i = 0;i<n;i++){
    pair<int,int> tmp;
    cin>>tmp.first>>tmp.second;
    p.insert(tmp);
  }

  int ret = 0;
  for(auto it = p.begin();it!=p.end();it++){
    for(auto jt = next(it);jt!=p.end();jt++){
      auto d = (*it)-(*jt);
      swap(d.first,d.second);
      d.second*=-1;
      if(p.count((*it)+d) && p.count((*jt)+d)){
	ret = max(ret,(int)(pow(d.first,2)+pow(d.second,2)));
      }
      if(p.count((*it)-d) && p.count((*jt)-d)){
	ret = max(ret,(int)(pow(d.first,2)+pow(d.second,2)));
      }
    }
  }
  cout<<ret<<endl;
      

    

  return 0;
}
